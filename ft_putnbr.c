#include "minitalk.h"

void	ft_putnbr(int i)
{
	long long	s;

	s = i;
	if (s < 0)
	{
		ft_putchar('-');
		s *= -1;
	}
	if (s < 10)
	{
		ft_putchar((s % 10) + 48);
	}
	else
	{
		ft_putnbr(s / 10);
		ft_putchar((s % 10) + 48);
	}
}
